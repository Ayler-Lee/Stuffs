//: While 循环

/*
while循环运行一系列语句直到条件变成false。这类循环适合使用在第一次迭代前迭代次数未知的情况下。Swift 提供两种while循环形式：

while循环，每次在循环开始时计算条件是否符合；
do-while循环，每次在循环结束时计算条件是否符合。
*/

/*: While
    while condition {
    statements
    }
*/


/*:
    do {
    statements
    } while condition
*/

